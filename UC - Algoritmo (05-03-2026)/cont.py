dados = [10, 20, 30, 60, 30]
print(dados)

# números de elementos
print("Comprimentos: ", len(dados))

# conta ocorrências
print("Qtds vezes o 30 aparece: ", dados.count(30))

# index - encontrar posição
print("índice de 20: ", dados.index(20))

# in verificar a existência
print("20 está na lista? ", 20 in dados)
print("100 está na lista? ", 100 in dados)